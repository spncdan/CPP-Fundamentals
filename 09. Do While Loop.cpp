// Do While Loop

#include <iostream>
using namespace std;

int main() {
    
    // Do While Loop, allows you to do an action while a particular condition is true
    
    // Print numbers from 1 to 5
    
    int i = 1;
    
    do {
        cout << i << endl;
        i++;
    } while (i <= 5); // Loops as long as this condition is true
    
    // Do While loops generally guarantee that the loop will run at least once
    
    // Switch statements are NOT loops

    return 0;
}

